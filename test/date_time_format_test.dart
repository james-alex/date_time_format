void main() {
  final dateTimes = <DateTime>[
    DateTime.utc(2015, 1, 1, 1, 1, 1, 1, 1),
    DateTime.utc(2016, 2, 29, 3, 12),
    DateTime.utc(2017, 3, 19, 17, 23, 30, 60, 323),
    DateTime.utc(2018, 4, 13, 12, 15, 48),
    DateTime.utc(2019, 5, 8, 9, 35, 16, 2, 200),
    DateTime.utc(2020, 6, 25, 22),
    DateTime.utc(2021, 7, 14, 15, 6, 55, 234),
    DateTime.utc(2022, 8, 16),
    DateTime.utc(2023, 9, 2, 23, 59, 59, 999, 999),
    DateTime.utc(2024, 10, 15, 19, 42, 5),
    DateTime.utc(2025, 11, 30, 12),
    DateTime.utc(2026, 12, 7),
  ];

  test('Utility Method Test', () {
    _testValues.forEach((format, expectedValues) {
      for (var i = 0; i < dateTimes.length; i++) {
        final result = DateTimeFormat.format(dateTimes[i], format: format);
        expect(expectedValues[i], equals(result));
      }
    });
  });

  test('Extension Method Test', () {
    _testValues.forEach((format, expectedValues) {
      for (var i = 0; i < dateTimes.length; i++) {
        final result = dateTimes[i].format(format);
        expect(expectedValues[i], equals(result));
      }
    });
  });
}

